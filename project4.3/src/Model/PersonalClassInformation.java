package Model;

public class PersonalClassInformation {

	private String Clas_year;
	private String Clas_no;
	private String Clas_name;
	private String Clas_div;
	private String Clas_prof;
	private String Clas_people;
	private String Clas_time1;
	private String Clas_time2;
	private String Clas_location;
	private String Clas_score;
	
	public PersonalClassInformation(String Clas_year, String Clas_no, String Clas_name, String Clas_div, String Clas_prof,
			String Clas_people, String Clas_time1, String Clas_time2, String Clas_location, String Clas_score) {
		super();
		this.Clas_year = Clas_year;
		this.Clas_no = Clas_no;
		this.Clas_name = Clas_name;
		this.Clas_div = Clas_div;
		this.Clas_prof = Clas_prof;
		this.Clas_people = Clas_people;
		this.Clas_time1 = Clas_time1;
		this.Clas_time2 = Clas_time2;
		this.Clas_location = Clas_location;
		this.Clas_score = Clas_score;
	}

	public String getClas_year() {
		return Clas_year;
	}

	public void setClas_year(String clas_year) {
		Clas_year = clas_year;
	}

	public String getClas_no() {
		return Clas_no;
	}

	public void setClas_no(String clas_no) {
		Clas_no = clas_no;
	}

	public String getClas_name() {
		return Clas_name;
	}

	public void setClas_name(String clas_name) {
		Clas_name = clas_name;
	}

	public String getClas_div() {
		return Clas_div;
	}

	public void setClas_div(String clas_div) {
		Clas_div = clas_div;
	}

	public String getClas_prof() {
		return Clas_prof;
	}

	public void setClas_prof(String clas_prof) {
		Clas_prof = clas_prof;
	}

	public String getClas_people() {
		return Clas_people;
	}

	public void setClas_people(String clas_people) {
		Clas_people = clas_people;
	}

	public String getClas_time1() {
		return Clas_time1;
	}

	public void setClas_time1(String clas_time1) {
		Clas_time1 = clas_time1;
	}

	public String getClas_time2() {
		return Clas_time2;
	}

	public void setClas_time2(String clas_time2) {
		Clas_time2 = clas_time2;
	}

	public String getClas_location() {
		return Clas_location;
	}

	public void setClas_location(String clas_location) {
		Clas_location = clas_location;
	}

	public String getClas_score() {
		return Clas_score;
	}

	public void setClas_score(String clas_score) {
		Clas_score = clas_score;
	}

}
